//for loop 
#include<stdio.h>
void main()
{
	int a;
	for(a=5;a>=0;a--)
	{
		printf("The value of a is %d\n",a);
	}
	printf("Loop ended here.\n");
}
/*OUTPUT:
The value of a is 5
The value of a is 4
The value of a is 3
The value of a is 2
The value of a is 1
The value of a is 0
Loop ended here.	*/